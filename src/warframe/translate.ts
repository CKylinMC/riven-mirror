/**
 * 翻译工具类
 *
 * @export
 * @class Translator
 */
export class Translator {
  public static Locale: "zh" | "en" = "zh";
  protected static instance = new Translator();
  private mainDict: Map<string, string>;
  private subDict: Map<string, string>;
  private subLinkDict: Map<string, string[]>;

  constructor() {
    // 英中字典
    this.mainDict = new Map(_transData.map(v => [v.id.toLowerCase(), v.name] as [string, string]));
    // 后缀字典
    this.subDict = new Map(_partSubfixs.map(v => [v[0].toLowerCase(), v[1]] as [string, string]));
    // 后缀辅助字典
    this.subLinkDict = new Map();
    _partSubfixs.forEach(v => {
      if (!v[0].includes(" ")) return;
      let sub = _.last(v[0].split(" "));
      if (this.subLinkDict.has(sub)) this.subLinkDict.get(sub.toLowerCase()).push(v[0]);
      else this.subLinkDict.set(sub.toLowerCase(), [v[0]]);
    });
  }

  /**
   * 翻译文本
   *
   * @export
   * @param {string} text 原文本
   * @returns {string} 翻译后的文本
   */
  static getLocText(rawText: string): string {
    if (this.Locale !== "zh") return rawText;
    // 忽略大小写
    let text = rawText.toLowerCase(), m;
    // 处理如 "100 Endo" => "100 内融核心"
    if (m = text.match(/^(\d+) (.+)/))
      return `${m[1]} ${this.getLocText(m[2])}`;
    // 处理如 "Weapon Restriction: Sniper Only" => "武器限定：狙击枪"
    if (text.includes(": "))
      return (([a, b]) => this.getLocText(a) + "：" + this.getLocText(b.replace(/ only$/i, "")))(rawText.split(": "));
    let lastWord = _.last(text.split(" "));
    // 辅助表查询
    let subfix = this.instance.subLinkDict.has(lastWord)
      && this.instance.subLinkDict.get(lastWord).find(v => text.endsWith(v[1]))
      || lastWord;
    // 主表查询
    let localeSubfix = this.instance.subDict.get(subfix);
    if (localeSubfix) {
      let len = text.length - subfix.length - 1;
      if (len < 0)
        return localeSubfix;
      // 递归解析后缀
      return this.getLocText(text.substr(0, len)) + " " + localeSubfix;
    }
    return this.instance.mainDict.get(text) || rawText;
  }
}
/**
 * 翻译文本(缩写形式)
 *
 * @export
 * @param {string} rawText 原文本
 * @returns {string} 翻译后的文本
 */
export function $T(rawText: string) { return Translator.getLocText(rawText); }
